package spring.vo;

public class Appl {
	private int appl_Num;
	private int appl_Ado_Num;
	private String appl_Ado_AniPic;
	private String appl_Name;
	private int appl_Gender;
	private int appl_Age;
	private String appl_Tel;
	private String appl_Email;
	private String appl_Contact;
	private String appl_Addr;
	private int appl_Mar;
	private String appl_Job;
	
	public Appl() {}
	
	
	public Appl(int appl_Num, int appl_Ado_Num, String appl_Ado_AniPic, String appl_Name, int appl_Gender, int appl_Age,
			String appl_Tel, String appl_Email, String appl_Contact, String appl_Addr, int appl_Mar, String appl_Job) {
		
		this.appl_Num = appl_Num;
		this.appl_Ado_Num = appl_Ado_Num;
		this.appl_Ado_AniPic = appl_Ado_AniPic;
		this.appl_Name = appl_Name;
		this.appl_Gender = appl_Gender;
		this.appl_Age = appl_Age;
		this.appl_Tel = appl_Tel;
		this.appl_Email = appl_Email;
		this.appl_Contact = appl_Contact;
		this.appl_Addr = appl_Addr;
		this.appl_Mar = appl_Mar;
		this.appl_Job = appl_Job;
	}
	public int getAppl_Num() {
		return appl_Num;
	}
	public void setAppl_Num(int appl_Num) {
		this.appl_Num = appl_Num;
	}
	public int getAppl_Ado_Num() {
		return appl_Ado_Num;
	}
	public void setAppl_Ado_Num(int appl_Ado_Num) {
		this.appl_Ado_Num = appl_Ado_Num;
	}
	public String getAppl_Ado_AniPic() {
		return appl_Ado_AniPic;
	}
	public void setAppl_Ado_AniPic(String appl_Ado_AniPic) {
		this.appl_Ado_AniPic = appl_Ado_AniPic;
	}
	public String getAppl_Name() {
		return appl_Name;
	}
	public void setAppl_Name(String appl_Name) {
		this.appl_Name = appl_Name;
	}
	public int getAppl_Gender() {
		return appl_Gender;
	}
	public void setAppl_Gender(int appl_Gender) {
		this.appl_Gender = appl_Gender;
	}
	public int getAppl_Age() {
		return appl_Age;
	}
	public void setAppl_Age(int appl_Age) {
		this.appl_Age = appl_Age;
	}
	public String getAppl_Tel() {
		return appl_Tel;
	}
	public void setAppl_Tel(String appl_Tel) {
		this.appl_Tel = appl_Tel;
	}
	public String getAppl_Email() {
		return appl_Email;
	}
	public void setAppl_Email(String appl_Email) {
		this.appl_Email = appl_Email;
	}
	public String getAppl_Contact() {
		return appl_Contact;
	}
	public void setAppl_Contact(String appl_Contact) {
		this.appl_Contact = appl_Contact;
	}
	public String getAppl_Addr() {
		return appl_Addr;
	}
	public void setAppl_Addr(String appl_Addr) {
		this.appl_Addr = appl_Addr;
	}
	public int getAppl_Mar() {
		return appl_Mar;
	}
	public void setAppl_Mar(int appl_Mar) {
		this.appl_Mar = appl_Mar;
	}
	public String getAppl_Job() {
		return appl_Job;
	}
	public void setAppl_Job(String appl_Job) {
		this.appl_Job = appl_Job;
	}
	
	
}
